      if (stream is null) return null;
      return new Bitmap(stream);
    }

    private static Icon 
    LoadIcon(string name)
    {
      Assembly asm = typeof(Common).Assembly;
      using Stream stream = asm.GetManifestResourceStream($"AVC.Resources.{name}.ico");
      if (stream is null) return null;
      using MemoryStream ms = new ();
      stream.CopyTo(ms);
      ms.Position = 0;
      return new Icon(ms);
    }

